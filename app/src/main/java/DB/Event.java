package DB;


public class Event
{
    private String thing;
    private String address;

    public String getThing()
    {
        return thing;
    }

    public String getAddress()
    {
        return address;
    }

    public void setThing(String thing)
    {
        this.thing = thing;
    }

    public void setAddress(String address)
    {
        this.address = address;
    }
}
